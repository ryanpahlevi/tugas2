public class tugas2 {
    public int nilaiHasil;
    
    int penjumlahan(int a, int b){
        nilaiHasil=a+b;
        return nilaiHasil;
    }
    int pengurangan(int a, int b){
        nilaiHasil=a-b;
        return nilaiHasil;
    }
    double pembagian(int a, int b){
        nilaiHasil=a/b;
        return nilaiHasil;
    }
    int perkalian(int a, int b){
        nilaiHasil=a*b;
        return nilaiHasil;
    }
}
